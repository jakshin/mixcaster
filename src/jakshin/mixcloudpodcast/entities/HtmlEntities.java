    /**
     * Unescapes HTML5 entities in the passed string, returning the result as a new string.
     *
     * @param str The string to unescape.
     * @return The unescaped string.
     */
    public String unescape(String str) {
        if (str == null) return null;

        final int len = str.length();
        StringBuilder sb = new StringBuilder(len);

        final int lastIndex = len - 1;
        int handledIndex = 0;  // first character in str not included into sb; all characters before it are "handled"
        int ampIndex;

        while ((ampIndex = str.indexOf('&', handledIndex)) != -1) {
            sb.append(str.substring(handledIndex, ampIndex));
            handledIndex = ampIndex;

            if (ampIndex < lastIndex && str.charAt(ampIndex + 1) == '#') {
                // numeric character reference
                int radix = 10;
                int numStartIndex = ampIndex + 2;
                if (numStartIndex >= lastIndex) break;  // at end of string

                if (Character.toLowerCase(str.charAt(numStartIndex)) == 'x') {
                    radix = 16;
                    numStartIndex++;
                    if (numStartIndex == lastIndex) break;  // at end of string
                }

                int numEndIndex = numStartIndex;
                while (numEndIndex <= lastIndex && Character.digit(str.charAt(numEndIndex), radix) != -1) {
                    numEndIndex++;
                }

                try {
                    String numStr = str.substring(numStartIndex, numEndIndex);
                    int num = Integer.parseInt(numStr, radix);
                    sb.append((char) num);

                    if (numEndIndex < lastIndex && str.charAt(numEndIndex) == ';') numEndIndex++;
                    handledIndex = numEndIndex;  // just past the semicolon, or last digit if there's no semicolon
                }
                catch (NumberFormatException ex) {
                    // invalid numeric character reference; pass it through unchanged
                    sb.append('&');
                    handledIndex = ampIndex + 1;  // just past the ampersand
                }
            }
            else {
                // character entity reference
                if (ampIndex + 2 > lastIndex) {
                    // the ampersand is too close to the end of the string
                    sb.append(str.substring(ampIndex));
                    handledIndex = lastIndex;  // end of the string
                    break;
                }

                boolean validEntity = false;
                final String start = str.substring(ampIndex, ampIndex + 3);  // ampersand plus 2 chars

                List<String> entities = this.filterMapKeys(HtmlEntities.entityToChar, new IFilterPredicate<String>() {
                   @Override
                   public boolean keep(String key) {
                       return key.startsWith(start);
                   }
                });

                // sort entities longest first, so e.g. "&gtcc;" and "&gt;" will be checked before "&gt"
                Collections.sort(entities, new Comparator<String>() {
                   @Override
                   public int compare(String str1, String str2) {
                       if (str1 == null) {
                           if (str2 == null) return 0;
                           else return 1;  // put nulls at the end
                       }
                       else if (str2 == null) {
                           return -1;
                       }

                       // longer strings first
                       return str2.length() - str1.length();
                   }
                });

                for (String entity : entities) {
                    int endIndex = ampIndex + entity.length();

                    if (endIndex <= len && str.substring(ampIndex, endIndex).equals(entity)) {
                        validEntity = true;
                        sb.append(HtmlEntities.entityToChar.get(entity));
                        handledIndex = endIndex; // just past the entity
                        break;
                    }
                }

                if (!validEntity) {
                    sb.append('&');
                    handledIndex = ampIndex + 1;  // just past the ampersand
                }
            }
        }

        if (handledIndex < lastIndex) {
            // get the leftovers
            sb.append(str.substring(handledIndex));
        }

        return sb.toString();
    }

    /**
     * Generic predicate interface for filtering.
     * @param <T> The generic type.
     */
    private interface IFilterPredicate<T> {
        public boolean keep(T mapKey);
    }

    /**
     * Returns a filtered list of a map's keys.
     *
     * @param <KT> The map key type; will be the returned list's type.
     * @param <VT> The map value type.
     * @param map The map.
     * @param predicate The predicate implementation which decides whether to keep keys while filtering.
     * @return A list containing the map keys which pass the filter predicate.
     */
    private <KT,VT> List<KT> filterMapKeys(Map<KT,VT> map, IFilterPredicate<KT> predicate) {
        // see http://stackoverflow.com/questions/122105/what-is-the-best-way-to-filter-a-java-collection
        List<KT> filteredKeys = new LinkedList<>();

        for (KT key : map.keySet()) {
            if (predicate.keep(key)) {
                filteredKeys.add(key);
            }
        }

        return filteredKeys;
    }

